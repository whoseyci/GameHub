#!/usr/bin/env node
// build-client-games.mjs — bundle the shared rules engine for the browser.
//
// Compiles src/client-games.ts (which imports the same GameModules the server
// uses) into a single classic <script> that exposes window.GameModules,
// window.GameCatalogue, and window.LocalEngines. index.html loads the output
// (public/js/00-game-modules.js) BEFORE the per-game client renderers.
//
// Run: node scripts/build-client-games.mjs            (one-shot)
//      node scripts/build-client-games.mjs --watch    (rebuild on change)
//      node scripts/build-client-games.mjs --check     (build to a temp buffer;
//                                                        fail if it would differ
//                                                        from the committed file)

import { build } from "esbuild";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const entry = join(root, "src", "client-games.ts");
const outFile = join(root, "public", "js", "00-game-modules.js");

const BANNER =
  "/* AUTO-GENERATED by scripts/build-client-games.mjs — DO NOT EDIT BY HAND.\n" +
  "   Source: src/client-games.ts (+ the shared src/games/* rules engine).\n" +
  "   Rebuild: npm run build:client-games */\n";

const common = {
  entryPoints: [entry],
  bundle: true,
  format: "iife",
  platform: "browser",
  target: ["es2020"],
  banner: { js: BANNER },
  legalComments: "none",
  logLevel: "info",
};

async function buildToString() {
  const result = await build({ ...common, write: false, minify: false });
  return result.outputFiles[0].text;
}

const args = process.argv.slice(2);

if (args.includes("--check")) {
  const generated = await buildToString();
  let existing = "";
  try {
    existing = readFileSync(outFile, "utf8");
  } catch {
    /* missing */
  }
  if (existing.trim() !== generated.trim()) {
    console.error(
      "✖ public/js/00-game-modules.js is stale. Run `npm run build:client-games` and commit the result."
    );
    process.exit(1);
  }
  console.log("✓ client-games bundle is up to date.");
} else if (args.includes("--watch")) {
  const ctx = await (await import("esbuild")).context({ ...common, outfile: outFile });
  await ctx.watch();
  console.log("Watching src/client-games.ts …");
} else {
  const text = await buildToString();
  writeFileSync(outFile, text);
  console.log(`✓ Wrote ${outFile} (${(text.length / 1024).toFixed(1)} KiB)`);
}
